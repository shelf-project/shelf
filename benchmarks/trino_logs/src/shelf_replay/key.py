"""Content-addressed key derivation — matches ``shelfd::store::key_from_tuple``.

Hash input (byte-for-byte with the Rust ``key_from_tuple`` and Java
``Key.fromTuple``)::

    sha256( etag_utf8 || offset_u64_le || length_u64_le || ordinal_u32_le )

The shared golden vectors live at
``shelfd/tests/fixtures/shelf04_golden_vectors.txt`` — regenerated by
``shelf/tools/gen_shelf04_golden.py``. Drift on any of the four
implementations (Rust / Java / generator / this module) fails CI on
both lanes.
"""

from __future__ import annotations

import hashlib


def content_key(etag: str, rg_ordinal: int, offset: int, length: int) -> str:
    """Return the 64-hex content key for a row-group byte range.

    Argument order matches :func:`shelf_replay.types.ScanResult.rg_entries`
    tuples — ``(rg_ordinal, offset, length)`` — even though the wire
    hash order is ``offset || length || ordinal``. The mapping is
    handled internally so callers do not need to care.
    """

    if rg_ordinal < 0 or rg_ordinal > 0xFFFF_FFFF:
        raise ValueError(f"rg_ordinal out of range: {rg_ordinal}")
    if offset < 0 or offset > 0xFFFF_FFFF_FFFF_FFFF:
        raise ValueError(f"offset out of range: {offset}")
    if length < 0 or length > 0xFFFF_FFFF_FFFF_FFFF:
        raise ValueError(f"length out of range: {length}")

    h = hashlib.sha256()
    h.update(etag.encode("utf-8"))
    h.update(offset.to_bytes(8, "little", signed=False))
    h.update(length.to_bytes(8, "little", signed=False))
    h.update(rg_ordinal.to_bytes(4, "little", signed=False))
    return h.hexdigest()
